# Leia as duas notas do aluno, somar e calcular a média;
print('Vou te ajudar a calular a média dos alunos. ')
print('Será bem simples. Coloque uma nota por vez que o resultado sairá no final.')
nota1 = float(input('A primeira nota: '))
nota2 = float(input('A segunda nota: '))
print('Agora que você já colocou as notas, vamos calcular.')
print('A soma das notas é: {};\nCom isso a média fica em: {:.2f};'.format((nota1 + nota2), ((nota1 + nota2)/2)))

